private void ProgressBarStart(){
    Timer mTimer = new Timer(45, (ActionEvent e) -> {
        pbLoading.setValue(pbLoading.getValue() + 1);
        pbLoading.setBackground(Color.orange);
        pbLoading.setStringPainted(true);
        pbLoading.setString("Loading..." + pbLoading.getValue() + "%");
    
    });   
    mTimer.start();
}
